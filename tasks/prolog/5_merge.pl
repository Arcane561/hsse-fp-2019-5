mrg([], L2, L2).
mrg(L1, [], L1).
mrg([H1|T1], [H2|T2], [H1|TR]) :- mrg(T1, [H2|T2], TR), H1 =< H2.
mrg([H1|T1], [H2|T2], [H2|TR]) :- mrg([H1|T1], T2, TR), H2 =< H1.

% ?- mrg([], [2, 4, 5], X).
% X = [2, 4, 5] .
%
% ?- mrg([1, 2, 4], [], X).
% X = [1, 2, 4] 
% 
% ?- mrg([1,6,7], [2,4,6], X).
% X = [1, 2, 4, 6, 6, 7]